name="SamanaShrestha"
print(len(name))             #prints length of character
print(name.find("m"))        #finds the position of certain letter
print(name.capitalize())     #capitalizes first letter of the word
print(name.upper())          #UPPERCASE
print(name.lower())          #lowercase
print(name.isdigit())        #if digits True otherwise False
print(name.isalpha())        #if alphabet True otherwise False
print(name.count("a"))       #counts the letter in the word
print(name.replace("a","o")) #replaces letters